#include "ft_colle02.h"

char		*ft_recup(void)
{
	char	buf[2];
	char	*ret;

	while (read(0, buf, 1))
	{
		buf[1] = 0;
		ret = ft_strjoin(ret, buf);
	}
	return (ret);
}
